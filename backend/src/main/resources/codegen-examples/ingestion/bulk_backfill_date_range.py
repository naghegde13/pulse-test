# PULSE codegen example: BulkBackfill — date-range historical replay from
# a JDBC source into bronze.
#
# Distinct from SnapshotIngestion (full-table dump on a recurring schedule):
#   - BulkBackfill is one-shot or rare: replay history because we just
#     added a pipeline OR fixed an upstream defect and need to rehydrate.
#   - Strictly bounded by [BACKFILL_START, BACKFILL_END]; never leaves
#     the window open-ended. Both inputs accept either ISO date literals
#     or PULSE date mnemonics (BOM-12, EOM-1, PBD, etc.) — the resolver
#     handles both, so the same call site supports fixed and dynamic.
#   - Idempotent: re-running the same window cleanly overwrites with no
#     duplicates. Achieved via Delta replaceWhere on the chunk's date range
#     (or the format's equivalent for Iceberg / Parquet).
#   - Chunked by month to bound JDBC partition memory; tune __CHUNK_DAYS__
#     for source row density.
#
# Convention notes:
#   - __PLACEHOLDER__ tokens are substituted by CodeGenerationService at
#     codegen time. They are NOT env vars.
#   - Storage placeholders (#30) come from StoragePlaceholderResolver:
#       __TABLE_PATH__         object-store URI when format is path-based
#                              (delta | iceberg_external | iceberg_bq_managed | parquet)
#       __BQ_TABLE__           project.dataset.table for bq_native
#       __LAKE_FORMAT__        chosen format; drives the write branch below
#       __STORAGE_BACKEND__    'DPC' | 'GCP'
#   - Date inputs (BACKFILL_START_INPUT, BACKFILL_END_INPUT) are
#     RESOLVED AT RUNTIME by pulse_dates so a DAG run on 2026-05-15 vs
#     2027-05-15 produces the right window without regenerating code.
#   - PULSE_* env vars are runtime-only (Airflow per-run templates).
#   - Secrets resolve from the secret manager via gcp-sm:// references
#     surfaced as env vars by the Airflow connection layer; never literals.
#
# Generated by PULSE codegen against the BulkBackfill blueprint. The agent
# SHOULD pattern its proposed params and DAG wiring on this file.

