def plan_chunks(start, end, chunk_days):
    """Split [start, end] inclusive into chunks of chunk_days days.

    Each chunk is the unit of idempotent overwrite — if the job dies mid-chunk,
    re-running the same window overwrites only that chunk.
    """
    if start > end:
        raise ValueError(f"backfill_start_date {start} is after backfill_end_date {end}")
    chunks = []
    cursor = start
    while cursor <= end:
        chunk_end = min(cursor + timedelta(days=chunk_days - 1), end)
        chunks.append((cursor, chunk_end))
        cursor = chunk_end + timedelta(days=1)
    return chunks


def read_chunk(spark, chunk_start, chunk_end):
    """Read one chunk via JDBC.

    partitionColumn / lowerBound / upperBound / numPartitions force Spark to
    issue parallel JDBC reads; without them a multi-million-row chunk pins
    one executor for hours. The predicate is wrapped in a subquery so it
    pushes down to the database (avoiding re-issuing the same query for
    every partition).
    """
    predicate = (
        f"{DATE_COLUMN} >= DATE '{chunk_start.isoformat()}' "
        f"AND {DATE_COLUMN} <= DATE '{chunk_end.isoformat()}'"
    )
    log.info("reading chunk start=%s end=%s table=%s partitions=%d",
             chunk_start, chunk_end, JDBC_TABLE, JDBC_NUM_PARTITIONS)
    return (
        spark.read.format("jdbc")
        .option("url", JDBC_URL)
        .option("driver", JDBC_DRIVER)
        .option("user", JDBC_USER)
        .option("password", JDBC_PASSWORD)
        .option("dbtable", f"(SELECT * FROM {JDBC_TABLE} WHERE {predicate}) src")
        .option("partitionColumn", PRIMARY_KEY)
        .option("lowerBound", "0")
        .option("upperBound", str(2**31 - 1))
        .option("numPartitions", JDBC_NUM_PARTITIONS)
        .option("fetchsize", "10000")
        .load()
    )


def audit(df, chunk_start):
    """Add PULSE audit columns. Bronze MUST NOT transform — only audit
    columns are added. Any cleaning or casting belongs in the silver
    layer (BronzeToSilverCleaning)."""
    return (
        df
        .withColumn("ds", F.date_format(F.col(DATE_COLUMN), "yyyy-MM-dd"))
        .withColumn("_pulse_business_date", F.lit(chunk_start.isoformat()).cast(StringType()))
        .withColumn("_pulse_processing_ts", F.lit(PULSE_PROCESSING_TS))
        .withColumn("_pulse_run_id", F.lit(RUN_ID))
        .withColumn("_pulse_pipeline", F.lit(PIPELINE_NAME))
        .withColumn("_pulse_source", F.lit(f"{SOR_SLUG}/{JDBC_TABLE}"))
    )


def write_chunk(df, chunk_start, chunk_end):
    """Write one chunk to bronze with format-aware idempotency.

    Each format has its own idempotency primitive:
      - delta              → replaceWhere on the chunk's ds range
      - iceberg_external   → MERGE/overwrite_partitions semantics
      - iceberg_bq_managed → BQ-managed Iceberg via spark-bigquery-connector
      - parquet            → overwrite the matched ds partitions
    bq_native is gold-only (locked rule) and won't appear here.
    """
    audited = audit(df, chunk_start)
    replace_where = (
        f"ds >= '{chunk_start.isoformat()}' AND ds <= '{chunk_end.isoformat()}'"
    )

    written = audited.cache()
    row_count = written.count()
    log.info("writing chunk start=%s end=%s rows=%d format=%s",
             chunk_start, chunk_end, row_count, LAKE_FORMAT)

    if LAKE_FORMAT == "delta":
        # replaceWhere is THE idempotency primitive for chunked backfills:
        # overwrites only the partitions covered by the predicate, leaving
        # prior chunks (and any rows outside the window) untouched.
        (
            written.write
            .format("delta")
            .mode("overwrite")
            .option("replaceWhere", replace_where)
            .partitionBy("ds")
            .save(TABLE_PATH)
        )
    elif LAKE_FORMAT == "iceberg_external":
        # Iceberg's overwritePartitions rewrites only the matched ds
        # partitions. Requires the destination table to already exist
        # (PR-reviewed DDL artifact); this job assumes existence.
        written.createOrReplaceTempView("pulse_chunk")
        # Use SQL because the Spark Iceberg DataFrame API does not yet
        # expose overwritePartitions cleanly across all engines.
        written.sparkSession.sql(
            f"DELETE FROM iceberg.`{TABLE_PATH}` WHERE {replace_where}"
        )
        written.writeTo(f"iceberg.`{TABLE_PATH}`").append()
    elif LAKE_FORMAT == "iceberg_bq_managed":
        # BQ-managed Iceberg goes through the spark-bigquery-connector;
        # the connector accepts the table as a path under the connector's
        # configured BQ-managed-table mapping.
        (
            written.write
            .format("bigquery")
            .option("table", BQ_TABLE if BQ_TABLE else TABLE_PATH)
            .option("writeMethod", "indirect")
            .mode("append")
            .save()
        )
    elif LAKE_FORMAT == "parquet":
        # Plain parquet — no ACID metadata. Chunked overwrite is achieved
        # by writing each ds partition separately with mode='overwrite'
        # and dynamic-partition-overwrite enabled.
        (
            written.write
            .format("parquet")
            .mode("overwrite")
            .option("partitionOverwriteMode", "dynamic")
            .partitionBy("ds")
            .save(TABLE_PATH)
        )
    else:
        raise ValueError(
            f"Unsupported LAKE_FORMAT for BulkBackfill bronze: {LAKE_FORMAT}. "
            "Legal: delta | iceberg_external | iceberg_bq_managed | parquet."
        )

    written.unpersist()
    return row_count


def main():
    spark = (
        SparkSession.builder
        .appName(f"pulse-bulk-backfill-{PIPELINE_SLUG}-{RUN_ID}")
        .getOrCreate()
    )

    log.info(
        "BulkBackfill start. as_of=%s, window=[%s, %s] (resolved from inputs %r..%r), "
        "chunk_days=%d, calendar=%s, table=%s, backend=%s, format=%s",
        AS_OF_DATE, BACKFILL_START, BACKFILL_END,
        BACKFILL_START_INPUT, BACKFILL_END_INPUT,
        CHUNK_DAYS, HOLIDAY_CALENDAR_ID, JDBC_TABLE,
        STORAGE_BACKEND, LAKE_FORMAT,
    )
    chunks = plan_chunks(BACKFILL_START, BACKFILL_END, CHUNK_DAYS)
    log.info("planned %d chunks", len(chunks))

    total_rows = 0
    for chunk_start, chunk_end in chunks:
        df = read_chunk(spark, chunk_start, chunk_end)
        rows = write_chunk(df, chunk_start, chunk_end)
        total_rows += rows
        advance_high_water_mark(
            pipeline=PIPELINE_NAME,
            run_id=RUN_ID,
            high_water_mark=chunk_end.isoformat(),
            row_count=rows,
        )
        log.info("chunk done end=%s rows=%d cumulative=%d", chunk_end, rows, total_rows)

    log.info("BulkBackfill complete window=[%s, %s] total_rows=%d",
             BACKFILL_START, BACKFILL_END, total_rows)
    spark.stop()


if __name__ == "__main__":
    main()
